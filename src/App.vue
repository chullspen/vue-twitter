<template>
  <div id="app">
    @{{ user.userName }} - {{ fullName }}
    <p>followers: {{ followers }}</p>
    <button @click="followUser">
      Follow
    </button>
  </div>
</template>

<script>

export default {
  name: 'App',
  data() {
    return {
      followers: 0,
      user: {
        id: 1,
        userName: 'jefftoad',
        firstName: 'Jeff',
        lastName: 'Toad',
        isAdmin: true
      }
    }
  },
  computed: {
    fullName() {
      return `${this.user.firstName} ${this.user.lastName}`;
    }
  },
  methods: {
    followUser() {
      this.followers++
    }
  }
}
</script>

